// Returns character width
inline double drawChar(GraphicsManager& gm, char c, Vector2D pos, Vector2D dim) {
	c -= 32;
	if (c < 0 || (size_t)c >= charFont.size()) return 0;
	for (array<array<int, 2>, 2>& v : charFont[c].font) {
		Vector2D scaled[] = {
			Vector2D(pos[0][0] + v[0][0] * dim[0][0], pos[1][0] + v[0][1] * dim[1][0]),
			Vector2D(pos[0][0] + v[1][0] * dim[0][0], pos[1][0] + v[1][1] * dim[1][0]),
		};
		gm.plot(scaled, 2);
	}
	return charFont[c].width * dim[0][0];
}